if job_title == "cto" or job_title == "director":
    print("You have access!")
else:
    print("sorry, you don't have access")

# Elif
if job_title == "cto":
    print("Welcome CTO, you are awesome")
elif job_title == "director":
    print("Welcome Director, you are very kind")
else:
    print(job_title + " are not welcome here.")
